# To run this code, be sure your current working directory
# is the same as where this file is located and then run:
# ruby 3-arrays.rb

# Arrays are lists of things

foods = ["tacos", "pizza", "ice cream"]
p foods

mixed_array = ["tacos", 3, true]
p mixed_array

# Accessing the array

puts foods[0]
puts foods[-4]

# Add to the array

foods.push("salad")
foods << "fries"
p foods

