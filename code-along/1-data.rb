# In Ruby, there are different *types* of data:

# Numbers
puts 2 + 3

# Perform simple math with numbers
puts 2/3.0

# Strings
puts "Hello, World"

# Combine strings together

puts "Hello," + " World"
puts "Tacos" * 3

# Variables
food = "Tacos"
puts food

# Combine strings and variables

greeting = "heeeyyyy"
puts "#{greeting}, world"

